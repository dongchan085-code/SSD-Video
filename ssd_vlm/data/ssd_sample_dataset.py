"""
SSD Sample Dataset for LoRA fine-tuning.

Loads JSONL samples generated by generate_samples.py and prepares them
for causal language model training. Includes a 10% VQA regularization
buffer to prevent format overfitting to multiple-choice structures.
"""

import json
import logging
import random
from pathlib import Path
from typing import Any, Dict, List, Optional

import torch
from torch.utils.data import Dataset, DataLoader

logger = logging.getLogger(__name__)


class SSDSampleDataset(Dataset):
    """
    Dataset for SSD-generated samples (JSONL format).

    Each sample contains a video QA completion generated by the frozen
    base model at high temperature. A configurable fraction of samples
    is converted to open-ended VQA format (no multiple-choice options)
    to regularize against MC format overfitting.
    """

    def __init__(
        self,
        samples_path: str,
        tokenizer,
        max_seq_length: int = 4096,
        vqa_buffer_ratio: float = 0.1,
        seed: int = 42,
    ):
        """
        Args:
            samples_path: Path to JSONL file from generate_samples.py
            tokenizer: HuggingFace tokenizer
            max_seq_length: Maximum sequence length for tokenization
            vqa_buffer_ratio: Fraction of samples converted to open-ended
                VQA format (default 0.1 = 10%)
            seed: Random seed for VQA buffer selection
        """
        self.tokenizer = tokenizer
        self.max_seq_length = max_seq_length
        self.vqa_buffer_ratio = vqa_buffer_ratio

        # Load raw samples
        self.raw_samples = self._load_samples(samples_path)

        # Determine which samples become VQA buffer
        rng = random.Random(seed)
        n_vqa = int(len(self.raw_samples) * vqa_buffer_ratio)
        vqa_indices = set(rng.sample(range(len(self.raw_samples)), n_vqa))
        self.vqa_indices = vqa_indices

        logger.info(
            f"Loaded {len(self.raw_samples)} SSD samples "
            f"({n_vqa} as open-ended VQA buffer, "
            f"{len(self.raw_samples) - n_vqa} as MC format)"
        )

    @staticmethod
    def _load_samples(path: str) -> List[Dict[str, Any]]:
        """Load JSONL samples."""
        samples = []
        with open(path, "r") as f:
            for line in f:
                line = line.strip()
                if line:
                    samples.append(json.loads(line))
        return samples

    def __len__(self) -> int:
        return len(self.raw_samples)

    def _format_mc(self, sample: Dict[str, Any]) -> str:
        """Format as multiple-choice prompt + completion."""
        options = sample.get("options", [])
        options_text = "\n".join(
            f"{chr(65 + i)}: {opt}" for i, opt in enumerate(options)
        )
        prompt = (
            f"Question: {sample['question']}\n\n"
            f"Options:\n{options_text}\n\n"
            f"Answer:"
        )
        return prompt

    def _format_vqa(self, sample: Dict[str, Any]) -> str:
        """Format as open-ended VQA prompt (no options)."""
        prompt = (
            f"Question: {sample['question']}\n\n"
            f"Answer:"
        )
        return prompt

    def __getitem__(self, idx: int) -> Dict[str, torch.Tensor]:
        sample = self.raw_samples[idx]

        # Choose format: open-ended VQA or MC
        if idx in self.vqa_indices:
            prompt = self._format_vqa(sample)
        else:
            prompt = self._format_mc(sample)

        completion = sample.get("completion", "")
        full_text = prompt + " " + completion

        # Tokenize
        encoding = self.tokenizer(
            full_text,
            max_length=self.max_seq_length,
            truncation=True,
            padding="max_length",
            return_tensors="pt",
        )

        input_ids = encoding["input_ids"].squeeze(0)
        attention_mask = encoding["attention_mask"].squeeze(0)

        # Labels: mask prompt tokens with -100 so loss is only on completion
        prompt_encoding = self.tokenizer(
            prompt,
            max_length=self.max_seq_length,
            truncation=True,
            return_tensors="pt",
        )
        prompt_len = prompt_encoding["input_ids"].shape[1]

        labels = input_ids.clone()
        labels[:prompt_len] = -100
        # Also mask padding
        labels[attention_mask == 0] = -100

        return {
            "input_ids": input_ids,
            "attention_mask": attention_mask,
            "labels": labels,
        }


class SSDSampleDataCollator:
    """Simple data collator that stacks pre-tokenized samples."""

    def __call__(self, features: List[Dict[str, torch.Tensor]]) -> Dict[str, torch.Tensor]:
        return {
            "input_ids": torch.stack([f["input_ids"] for f in features]),
            "attention_mask": torch.stack([f["attention_mask"] for f in features]),
            "labels": torch.stack([f["labels"] for f in features]),
        }


def create_ssd_sample_dataloader(
    samples_path: str,
    tokenizer,
    batch_size: int = 2,
    num_workers: int = 4,
    shuffle: bool = True,
    max_seq_length: int = 4096,
    vqa_buffer_ratio: float = 0.1,
    pin_memory: bool = True,
    **kwargs,
) -> DataLoader:
    """
    Create a DataLoader for SSD samples.

    Args:
        samples_path: Path to JSONL samples
        tokenizer: HuggingFace tokenizer
        batch_size: Batch size
        num_workers: Number of data loading workers
        shuffle: Whether to shuffle
        max_seq_length: Maximum sequence length
        vqa_buffer_ratio: Fraction converted to open-ended VQA (default 10%)
        pin_memory: Pin memory for GPU transfer
    """
    dataset = SSDSampleDataset(
        samples_path=samples_path,
        tokenizer=tokenizer,
        max_seq_length=max_seq_length,
        vqa_buffer_ratio=vqa_buffer_ratio,
    )

    collator = SSDSampleDataCollator()

    return DataLoader(
        dataset,
        batch_size=batch_size,
        num_workers=num_workers,
        shuffle=shuffle,
        pin_memory=pin_memory,
        collate_fn=collator,
        drop_last=True,
    )
